// Binary Heap
    // Every node on the top level have a larger value than lower levels
    // Great at comparative operations
    // Will shift values if values is inserted and is largest variable in heap.
        // Worst case: Object(log n)
        // Best case: O(1)

// Time Consplexity
    // Lookup - O(n)
    // insert - O(log N)
    // delete - O(log N)

    // Pros
        // better than O(n)
        // priority
        // flexible size
        // fast insert

    // Coms
        // slow lookup